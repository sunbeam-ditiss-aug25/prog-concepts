# Boolean 
# - has only two values: True or False

# boolean data type
can_vote = True
print(f"can_vote = {can_vote}, type = {type(can_vote)}")

# boolean data type
can_vote = False
print(f"can_vote = {can_vote}, type = {type(can_vote)}")

# NoneType data type
my_var = None
print(f"my_var = {my_var}, type = {type(my_var)}")