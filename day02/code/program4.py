# collections

# data type is list
numbers = [10, 20, 30, 40, 50]
print(f"numbers = {numbers}, type = {type(numbers)}")

# data type is tuple
countries = ("india", "usa", "uk", "japan")
print(f"countries = {countries}, type = {type(countries)}")

# data type is tuple
colors = "black", "white", "yellow", "red"
print(f"colors = {colors}, type = {type(colors)}")

# data type is set
unique_values = {10, 20, 30, 40, 50, 10, 20, 30, 40, 50, 10, 20, 30, 40, 50}
print(f"unique_values = {unique_values}, type = {type(unique_values)}")

# data type is dictionary
person = {
    'name': 'amit', 
    'address': 'pune', 
    'phone': '+913434345',
    "age": 30
}
print(f"person = {person}, type = {type(person)}")